package org.accp.pojo;

public class Proot {
    private Integer prootid;

    private Integer rid;

    private Integer ppid;

    private Boolean prboolean;

    public Integer getProotid() {
        return prootid;
    }

    public void setProotid(Integer prootid) {
        this.prootid = prootid;
    }

    public Integer getRid() {
        return rid;
    }

    public void setRid(Integer rid) {
        this.rid = rid;
    }

    public Integer getPpid() {
        return ppid;
    }

    public void setPpid(Integer ppid) {
        this.ppid = ppid;
    }

    public Boolean getPrboolean() {
        return prboolean;
    }

    public void setPrboolean(Boolean prboolean) {
        this.prboolean = prboolean;
    }
}